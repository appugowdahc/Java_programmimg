public class IfElseIf{

    public static void main(String args[]){

        int x = 1;
        if (x==1){
            System.out.println("The value is one rupee, now you can buy chocolate");

        }else if(x==2){
            System.out.println("The value is two rupee, now you can purchase chocobar.");
        }else if(x==3){
            System.out.println("The value is three, now you get biscuit.");
        }else{
            System.out.println("You do not have money to buy anything.");
        }
    }
}