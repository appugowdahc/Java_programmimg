public class ArithmaticOps{

    public static void main(String a[]){
        int num1 = 100;
        int num2 = 200;
        int add = num1 + num2;
        int mult = num1 * num2;
        int div = num1 / num2;
        int sub = num1 - num2;
        int rem = num1 % num2;

    System.out.println("The addition of num1 and num2 value is " + add);
    System.out.println("The multiplication of num1 and num2 value is " + mult);
    System.out.println("The division of num1 and num2 value is " + div);
    System.out.println("The subtraction of num1 and num2 value is " + sub);
    System.out.println("The remainder of num1 and num2 value is " + rem);

    }
}